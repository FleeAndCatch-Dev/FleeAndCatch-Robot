package flee_and_catch.robot.localisation;

import flee_and_catch.robot.communication.command.device.robot.Position;

//### IMPORTS ##############################################################################################################################

public class PlayingField {

//### CONSTANTS ############################################################################################################################

//### ATTRIBUTES ###########################################################################################################################
	
	private float width;
	private float height;

//### COMPONENTS ###########################################################################################################################

//### CONSTRUCTORS #########################################################################################################################
	
	public PlayingField(float width, float height) {
		
		this.width = width;
		this.height = height;
	}
	
//### INITIAL METHODS ######################################################################################################################

//### INNER CLASSES ########################################################################################################################

//### GETTER/SETTER ########################################################################################################################

	public boolean isIn(Position pos) {
		return !(pos.getX() < 0 || pos.getX() > this.width || pos.getY() < 0 || pos.getY() > this.height);
	}
	
//### METHODS ##############################################################################################################################

//##########################################################################################################################################
}
//### EOF ##################################################################################################################################